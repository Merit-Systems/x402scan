import { checkEndpointSchema, getWarningsForL3 } from '@agentcash/discovery';
import type {
  AuditWarning,
  CheckEndpointResult,
  EndpointMethodAdvisory,
} from '@agentcash/discovery';
import { buildSampleBodyFromInputSchema, PROBE_TIMEOUT_MS } from './utils';

export type ProbeX402Result =
  | {
      success: true;
      advisory: EndpointMethodAdvisory;
      warnings: AuditWarning[];
    }
  | { success: false; error: string };

const METHOD_PREFERENCE = ['POST', 'GET', 'PUT', 'PATCH', 'DELETE'] as const;

export interface ProbeX402EndpointOptions {
  preferredMethod?: string;
  headers?: Record<string, string>;
  sampleInputBody?: Record<string, unknown>;
}

function normalizeProbeOptions(
  options?: string | ProbeX402EndpointOptions
): ProbeX402EndpointOptions {
  if (typeof options === 'string') return { preferredMethod: options };
  return options ?? {};
}

function pickX402Advisory(
  result: CheckEndpointResult,
  preferredMethod?: string
): EndpointMethodAdvisory | undefined {
  if (!result.found) return undefined;

  const candidates = result.advisories.filter(a =>
    a.paymentOptions?.some(p => p.protocol === 'x402')
  );

  // Prefer POST > GET > PUT > PATCH > DELETE; HEAD is excluded as it is
  // auto-generated by OpenAPI for every GET endpoint.
  const preferred =
    METHOD_PREFERENCE.flatMap(m => candidates.filter(a => a.method === m))[0] ??
    candidates[0];
  if (!preferred) return undefined;

  // If the OpenAPI spec declares a specific method, trust it over the probe.
  // Some APIs return 402 for all HTTP methods because their payment middleware
  // fires before method routing, so the probe may land on PATCH/PUT even
  // though the endpoint is declared as POST in the spec.
  if (preferredMethod && preferred.method !== preferredMethod.toUpperCase()) {
    return {
      ...preferred,
      method: preferredMethod.toUpperCase() as EndpointMethodAdvisory['method'],
    };
  }
  return preferred;
}

function pickInputSchemaFromSpec(
  result: CheckEndpointResult,
  preferredMethod?: string
): unknown {
  if (!result.found) return undefined;

  const target = preferredMethod?.toUpperCase();
  const byMethod = target
    ? result.advisories.find(a => a.method === target)
    : undefined;
  const advisory =
    byMethod ??
    METHOD_PREFERENCE.flatMap(m =>
      result.advisories.filter(a => a.method === m)
    )[0] ??
    result.advisories[0];

  return advisory?.inputSchema;
}

/**
 * Probes a URL and returns the first advisory that carries x402 payment options.
 *
 * Strategy:
 *   1. Probe directly, using a caller-provided sample body when present
 *      (works for servers whose paywall fires before request validation, and
 *      for programmatic registration where the caller knows the required body).
 *   2. If that yields no x402 advisory, fetch the OpenAPI advisory, build a
 *      minimum-valid sample body from its inputSchema, and re-probe with
 *      `sampleInputBody`. This handles servers (e.g. honcho) that validate
 *      input before the paywall and would otherwise return 400 to a probe.
 *
 * @param options - Optional preferred method, probe headers, and sample body.
 *   The legacy string form still supplies just the preferred method. When a
 *   preferred method is provided, it is preferred over what the probe lands on
 *   so probe-method drift (PATCH/PUT vs declared POST) does not corrupt the
 *   registered method.
 */
export async function probeX402Endpoint(
  url: string,
  options?: string | ProbeX402EndpointOptions
): Promise<ProbeX402Result> {
  const { preferredMethod, headers, sampleInputBody } =
    normalizeProbeOptions(options);

  const noBody = await checkEndpointSchema({
    url,
    headers,
    probe: true,
    ...(sampleInputBody ? { sampleInputBody } : {}),
    signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
  });

  const directAdvisory = pickX402Advisory(noBody, preferredMethod);
  if (directAdvisory) {
    return {
      success: true,
      advisory: directAdvisory,
      warnings: getWarningsForL3(directAdvisory),
    };
  }

  // Fallback: use OpenAPI inputSchema to build a sample body and re-probe.
  const spec = await checkEndpointSchema({
    url,
    headers,
    signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
  });
  const inputSchema = pickInputSchemaFromSpec(spec, preferredMethod);
  const fallbackInputBody = buildSampleBodyFromInputSchema(inputSchema);
  if (!fallbackInputBody) {
    return {
      success: false,
      error: noBodyError(noBody),
    };
  }

  const withBody = await checkEndpointSchema({
    url,
    headers,
    probe: true,
    sampleInputBody: fallbackInputBody,
    signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
  });
  const bodiedAdvisory = pickX402Advisory(withBody, preferredMethod);
  if (bodiedAdvisory) {
    return {
      success: true,
      advisory: bodiedAdvisory,
      warnings: getWarningsForL3(bodiedAdvisory),
    };
  }

  return {
    success: false,
    error:
      'No valid x402 response found (tried empty body and OpenAPI-derived sample body)',
  };
}

function noBodyError(result: CheckEndpointResult): string {
  if (!result.found) {
    const causeMessages = {
      not_found: 'Endpoint did not return a 402 payment challenge',
      network: 'Network error reaching endpoint',
      timeout: 'Endpoint timed out',
    } as const;
    return result.message ?? causeMessages[result.cause];
  }
  return 'No valid x402 response found';
}
