import { checkEndpointSchema, getWarningsForL3 } from '@agentcash/discovery';
import type {
  AuditWarning,
  CheckEndpointResult,
  EndpointMethodAdvisory,
} from '@agentcash/discovery';
import { Result } from 'better-result';

import { buildSampleBodyFromInputSchema, PROBE_TIMEOUT_MS } from './utils';
import { isLocalUrl } from '@/lib/url-helpers';

import {
  InvalidUrl,
  LocalUrlNotSupported,
  No402Challenge,
  ProbeNetworkError,
  ProbeTimeout,
  ProbeUnexpectedError,
  type ProbeError,
} from './errors';

export interface ProbeX402Ok {
  advisory: EndpointMethodAdvisory;
  warnings: AuditWarning[];
}

const METHOD_PREFERENCE = ['POST', 'GET', 'PUT', 'PATCH', 'DELETE'] as const;

function pickX402Advisory(
  result: CheckEndpointResult,
  preferredMethod?: string
): EndpointMethodAdvisory | undefined {
  if (!result.found) return undefined;

  const candidates = result.advisories.filter(a =>
    a.paymentOptions?.some(p => p.protocol === 'x402')
  );

  // Prefer POST > GET > PUT > PATCH > DELETE; HEAD is excluded as it is
  // auto-generated by OpenAPI for every GET endpoint.
  const preferred =
    METHOD_PREFERENCE.flatMap(m => candidates.filter(a => a.method === m))[0] ??
    candidates[0];
  if (!preferred) return undefined;

  // If the OpenAPI spec declares a specific method, trust it over the probe.
  // Some APIs return 402 for all HTTP methods because their payment middleware
  // fires before method routing, so the probe may land on PATCH/PUT even
  // though the endpoint is declared as POST in the spec.
  if (preferredMethod && preferred.method !== preferredMethod.toUpperCase()) {
    return {
      ...preferred,
      method: preferredMethod.toUpperCase() as EndpointMethodAdvisory['method'],
    };
  }
  return preferred;
}

function pickInputSchemaFromSpec(
  result: CheckEndpointResult,
  preferredMethod?: string
): unknown {
  if (!result.found) return undefined;

  const target = preferredMethod?.toUpperCase();
  const byMethod = target
    ? result.advisories.find(a => a.method === target)
    : undefined;
  const advisory =
    byMethod ??
    METHOD_PREFERENCE.flatMap(m =>
      result.advisories.filter(a => a.method === m)
    )[0] ??
    result.advisories[0];

  return advisory?.inputSchema;
}

/**
 * Probes a URL and returns the first advisory that carries x402 payment options.
 *
 * Strategy:
 *   1. Probe with no body (works for servers whose paywall fires before
 *      request validation).
 *   2. If that yields no x402 advisory, fetch the OpenAPI advisory, build a
 *      minimum-valid sample body from its inputSchema, and re-probe with
 *      `sampleInputBody`. This handles servers (e.g. honcho) that validate
 *      input before the paywall and would otherwise return 400 to a probe.
 *
 * @param preferredMethod - HTTP method from OpenAPI discovery. When provided
 *   it is preferred over what the probe lands on so probe-method drift
 *   (PATCH/PUT vs declared POST) does not corrupt the registered method.
 */
export async function probeX402Endpoint(
  url: string,
  preferredMethod?: string
): Promise<Result<ProbeX402Ok, ProbeError>> {
  // Validate the URL before any network calls so the failure surface stays
  // typed end-to-end.
  let parsed: URL;
  try {
    parsed = new URL(url);
  } catch {
    return Result.err(
      new InvalidUrl({ url, message: `"${url}" is not a valid URL` })
    );
  }
  if (parsed.protocol !== 'http:' && parsed.protocol !== 'https:') {
    return Result.err(
      new InvalidUrl({
        url,
        message: `Unsupported protocol ${parsed.protocol}; expected http(s)://`,
      })
    );
  }
  if (isLocalUrl(url)) {
    return Result.err(
      new LocalUrlNotSupported({
        url,
        message: 'Local and private URLs are not supported',
      })
    );
  }

  const noBody = await Result.tryPromise({
    try: () =>
      checkEndpointSchema({
        url,
        probe: true,
        signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
      }),
    catch: cause =>
      new ProbeUnexpectedError({
        url,
        message:
          cause instanceof Error
            ? `Probe threw: ${cause.message}`
            : `Probe threw: ${String(cause)}`,
      }),
  });

  if (Result.isError(noBody)) return Result.err(noBody.error);

  const directAdvisory = pickX402Advisory(noBody.value, preferredMethod);
  if (directAdvisory) {
    return Result.ok({
      advisory: directAdvisory,
      warnings: getWarningsForL3(directAdvisory),
    });
  }

  // Fallback: use OpenAPI inputSchema to build a sample body and re-probe.
  const spec = await Result.tryPromise({
    try: () =>
      checkEndpointSchema({
        url,
        signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
      }),
    catch: cause =>
      new ProbeUnexpectedError({
        url,
        message:
          cause instanceof Error
            ? `OpenAPI probe threw: ${cause.message}`
            : `OpenAPI probe threw: ${String(cause)}`,
      }),
  });

  if (Result.isError(spec)) {
    // Spec fetch failed — fall back to the no-body cause if it's informative.
    return Result.err(noBodyError(noBody.value, url, false));
  }

  const inputSchema = pickInputSchemaFromSpec(spec.value, preferredMethod);
  const sampleInputBody = buildSampleBodyFromInputSchema(inputSchema);
  if (!sampleInputBody) {
    return Result.err(noBodyError(noBody.value, url, false));
  }

  const withBody = await Result.tryPromise({
    try: () =>
      checkEndpointSchema({
        url,
        probe: true,
        sampleInputBody,
        signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
      }),
    catch: cause =>
      new ProbeUnexpectedError({
        url,
        message:
          cause instanceof Error
            ? `Sampled probe threw: ${cause.message}`
            : `Sampled probe threw: ${String(cause)}`,
      }),
  });

  if (Result.isError(withBody)) return Result.err(withBody.error);

  const bodiedAdvisory = pickX402Advisory(withBody.value, preferredMethod);
  if (bodiedAdvisory) {
    return Result.ok({
      advisory: bodiedAdvisory,
      warnings: getWarningsForL3(bodiedAdvisory),
    });
  }

  return Result.err(noBodyError(withBody.value, url, true));
}

function noBodyError(
  result: CheckEndpointResult,
  url: string,
  triedSampleBody: boolean
): ProbeError {
  if (!result.found) {
    switch (result.cause) {
      case 'not_found':
        return new No402Challenge({
          url,
          triedSampleBody,
          message:
            result.message ?? 'Endpoint did not return a 402 payment challenge',
        });
      case 'network':
        return new ProbeNetworkError({
          url,
          message: result.message ?? 'Network error reaching endpoint',
        });
      case 'timeout':
        return new ProbeTimeout({
          url,
          timeoutMs: PROBE_TIMEOUT_MS,
          message: result.message ?? 'Endpoint timed out',
        });
    }
  }
  return new No402Challenge({
    url,
    triedSampleBody,
    message: 'Endpoint responded but advertised no x402 payment options',
  });
}
