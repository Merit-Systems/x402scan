import {
  checkEndpointSchema,
  getWarningsForL3,
  validatePaymentRequiredDetailed,
} from '@agentcash/discovery';
import type {
  AuditWarning,
  CheckEndpointResult,
  EndpointMethodAdvisory,
} from '@agentcash/discovery';
import https from 'node:https';
import {
  buildMinimalSampleFromInputSchema,
  buildMinimalQueryParamsFromInputSchema,
  PROBE_TIMEOUT_MS,
} from './utils';

/**
 * Direct HTTPS probe that tolerates large response headers (128 KB).
 * Some merchants embed the full 402 payment body in a `payment-required`
 * header, exceeding Node's default 16 KB limit. The discovery library's
 * internal `fetch` silently drops these as network errors.
 */
function directProbe402(
  url: string,
  method: string,
  timeoutMs: number
): Promise<{ status: number; body: unknown } | null> {
  return new Promise(resolve => {
    const parsed = new URL(url);
    const req = https.request(
      {
        hostname: parsed.hostname,
        port: parsed.port || 443,
        path: parsed.pathname + parsed.search,
        method,
        headers: { Accept: 'application/json' },
        timeout: timeoutMs,
        maxHeaderSize: 128 * 1024,
      },
      res => {
        const chunks: Buffer[] = [];
        res.on('data', (chunk: Buffer) => chunks.push(chunk));
        res.on('end', () => {
          try {
            const body: unknown = JSON.parse(Buffer.concat(chunks).toString());
            resolve({ status: res.statusCode ?? 0, body });
          } catch {
            resolve(null);
          }
        });
      }
    );
    req.on('error', () => resolve(null));
    req.on('timeout', () => {
      req.destroy();
      resolve(null);
    });
    req.end();
  });
}

export type ProbeX402Result =
  | {
      success: true;
      advisory: EndpointMethodAdvisory;
      warnings: AuditWarning[];
    }
  | {
      success: false;
      error: string;
      skipped?: boolean;
      /** HTTP status code from the probe attempt, when available. */
      statusCode?: number;
    };

const METHOD_PREFERENCE = ['POST', 'GET', 'PUT', 'PATCH', 'DELETE'] as const;

function pickX402Advisory(
  result: CheckEndpointResult,
  preferredMethod?: string
): EndpointMethodAdvisory | undefined {
  if (!result.found) return undefined;

  const candidates = result.advisories.filter(a =>
    a.paymentOptions?.some(p => p.protocol === 'x402')
  );

  // Prefer POST > GET > PUT > PATCH > DELETE; HEAD is excluded as it is
  // auto-generated by OpenAPI for every GET endpoint.
  const preferred =
    METHOD_PREFERENCE.flatMap(m => candidates.filter(a => a.method === m))[0] ??
    candidates[0];
  if (!preferred) return undefined;

  // If the OpenAPI spec declares a specific method, trust it over the probe.
  // Some APIs return 402 for all HTTP methods because their payment middleware
  // fires before method routing, so the probe may land on PATCH/PUT even
  // though the endpoint is declared as POST in the spec.
  if (preferredMethod && preferred.method !== preferredMethod.toUpperCase()) {
    return {
      ...preferred,
      method: preferredMethod.toUpperCase() as EndpointMethodAdvisory['method'],
    };
  }
  return preferred;
}

function pickInputSchemaFromSpec(
  result: CheckEndpointResult,
  preferredMethod?: string
): unknown {
  if (!result.found) return undefined;

  const target = preferredMethod?.toUpperCase();
  const byMethod = target
    ? result.advisories.find(a => a.method === target)
    : undefined;
  const advisory =
    byMethod ??
    METHOD_PREFERENCE.flatMap(m =>
      result.advisories.filter(a => a.method === m)
    )[0] ??
    result.advisories[0];

  return advisory?.inputSchema;
}

const RATE_LIMIT_STATUSES = new Set([429, 503]);
const MAX_RETRIES = 2;
const RETRY_BASE_MS = 1500;

/** Sleep for a given number of milliseconds. */
function sleep(ms: number): Promise<void> {
  return new Promise(resolve => setTimeout(resolve, ms));
}

/**
 * Probes a URL and returns the first advisory that carries x402 payment options.
 * Retries up to MAX_RETRIES times on 429/503 with exponential backoff.
 *
 * Strategy:
 *   1. Probe with no body (works for servers whose paywall fires before
 *      request validation).
 *   2. If that yields no x402 advisory and a merchant-provided sampleBody is
 *      available, re-probe with it. This handles servers that validate input
 *      before the paywall and would otherwise return 400 to a bare probe.
 *
 * @param preferredMethod - HTTP method from OpenAPI discovery. When provided
 *   it is preferred over what the probe lands on so probe-method drift
 *   (PATCH/PUT vs declared POST) does not corrupt the registered method.
 * @param sampleBody - Merchant-provided sample request body to use if the
 *   bare probe fails. Passed directly to checkEndpointSchema as sampleInputBody.
 */
export async function probeX402Endpoint(
  url: string,
  preferredMethod?: string,
  sampleBody?: Record<string, unknown>
): Promise<ProbeX402Result> {
  for (let attempt = 0; attempt <= MAX_RETRIES; attempt++) {
    const result = await probeX402EndpointOnce(
      url,
      preferredMethod,
      sampleBody
    );

    // Retry on rate limiting (429/503) with exponential backoff.
    if (
      !result.success &&
      result.statusCode !== undefined &&
      RATE_LIMIT_STATUSES.has(result.statusCode) &&
      attempt < MAX_RETRIES
    ) {
      await sleep(RETRY_BASE_MS * 2 ** attempt);
      continue;
    }

    return result;
  }

  // Unreachable, but satisfies the type checker.
  return { success: false, error: 'Max retries exceeded' };
}

async function probeX402EndpointOnce(
  url: string,
  preferredMethod?: string,
  sampleBody?: Record<string, unknown>
): Promise<ProbeX402Result> {
  const noBody = await checkEndpointSchema({
    url,
    probe: true,
    signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
  });

  const directAdvisory = pickX402Advisory(noBody, preferredMethod);
  if (directAdvisory) {
    return {
      success: true,
      advisory: directAdvisory,
      warnings: getWarningsForL3(directAdvisory),
    };
  }

  // Fallback: re-probe with a body and/or query params from OpenAPI spec.
  // Priority: merchant-provided sample > schema-derived minimal sample > {}
  let fallbackBody: Record<string, unknown> = sampleBody ?? {};
  let probeUrl = url;
  let inputSchema: unknown;

  if (!sampleBody) {
    // Fetch the OpenAPI spec to extract inputSchema for minimal sampling.
    const spec = await checkEndpointSchema({
      url,
      signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
    });
    inputSchema = pickInputSchemaFromSpec(spec, preferredMethod);
    fallbackBody = buildMinimalSampleFromInputSchema(inputSchema) ?? {};

    // Also sample required query params (for GET endpoints).
    const queryParams = buildMinimalQueryParamsFromInputSchema(inputSchema);
    if (queryParams) {
      const u = new URL(url);
      for (const [key, value] of Object.entries(queryParams)) {
        u.searchParams.set(key, value);
      }
      probeUrl = u.toString();
    }
  }

  const withBody = await checkEndpointSchema({
    url: probeUrl,
    probe: true,
    sampleInputBody: fallbackBody,
    signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
  });
  const bodiedAdvisory = pickX402Advisory(withBody, preferredMethod);
  if (bodiedAdvisory) {
    return {
      success: true,
      advisory: bodiedAdvisory,
      warnings: getWarningsForL3(bodiedAdvisory),
    };
  }

  // Fallback: direct HTTPS probe with large-header tolerance.
  // Some merchants embed the full 402 body in a `payment-required` header,
  // exceeding Node's default 16 KB limit. The discovery library's probes
  // fail silently on these endpoints.
  const directMethod = preferredMethod?.toUpperCase() ?? 'GET';
  const direct = await directProbe402(probeUrl, directMethod, PROBE_TIMEOUT_MS);
  if (direct?.status === 402 && direct.body) {
    // Validate the 402 body and extract normalized accepts.
    const validated = validatePaymentRequiredDetailed(direct.body);
    if (validated.valid && validated.normalized) {
      // Build advisory from OpenAPI (schemas) + direct 402 body (payment options).
      // The library's probe failed due to header overflow, but OpenAPI may
      // still have schema info we can merge in.
      const retry = await checkEndpointSchema({
        url: probeUrl,
        signal: AbortSignal.timeout(PROBE_TIMEOUT_MS),
      });
      const openApiAdvisory = pickX402Advisory(retry, preferredMethod);

      // Extract payment options from the raw 402 body's accepts array.
      // These are already in the x402 wire format — the library normally
      // parses them from the probe response, but header overflow prevented it.
      const rawBody = direct.body as Record<string, unknown>;
      const rawAccepts = Array.isArray(rawBody.accepts) ? rawBody.accepts : [];
      const paymentOptions = rawAccepts.map(accept => ({
        protocol: 'x402' as const,
        ...(accept as Record<string, unknown>),
      })) as unknown as EndpointMethodAdvisory['paymentOptions'];

      const advisory: EndpointMethodAdvisory = {
        source: 'probe',
        method: directMethod as EndpointMethodAdvisory['method'],
        paymentOptions,
        paymentRequiredBody: direct.body,
        ...(openApiAdvisory?.inputSchema
          ? { inputSchema: openApiAdvisory.inputSchema }
          : {}),
        ...(openApiAdvisory?.outputSchema
          ? { outputSchema: openApiAdvisory.outputSchema }
          : {}),
        ...(openApiAdvisory?.summary
          ? { summary: openApiAdvisory.summary }
          : {}),
      };

      const warnings = getWarningsForL3(advisory);
      warnings.push({
        code: 'HEADERS_OVERFLOW',
        severity: 'warn',
        message:
          'Response headers exceed 16 KB — the payment-required header may be too large. ' +
          'Some clients will fail to parse this response. ' +
          'Consider moving the full payment body to the 402 JSON response only.',
      });
      return { success: true, advisory, warnings };
    }
  }

  const isUnreachable =
    !noBody.found && (noBody.cause === 'network' || noBody.cause === 'timeout');

  // Capture the actual HTTP status code for error reporting.
  let statusCode: number | undefined;
  if (!isUnreachable) {
    try {
      const res = await fetch(probeUrl, {
        method: 'HEAD',
        signal: AbortSignal.timeout(5000),
      });
      statusCode = res.status;
    } catch {
      // Ignore — status code is best-effort.
    }
  }

  return {
    success: false,
    error: probeErrorMessage(noBody, withBody),
    skipped: !isUnreachable,
    statusCode,
  };
}

function probeErrorMessage(
  noBody: CheckEndpointResult,
  withBody?: CheckEndpointResult
): string {
  // Prefer the withBody result message since it's the final probe attempt.
  const result = withBody ?? noBody;
  if (!result.found) {
    const causeMessages = {
      not_found: 'Endpoint did not return a 402 payment challenge',
      network: 'Network error reaching endpoint',
      timeout: 'Endpoint timed out',
    } as const;
    const base = causeMessages[result.cause];
    // The library message often includes the status code (e.g. "got 400")
    return result.message ?? base;
  }
  return 'No valid x402 response found';
}
